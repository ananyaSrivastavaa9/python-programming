# for loop basic
name = "Ananya"
for i in name:
    print(name)
    if (i == "n"):
        print("wow")

# for loop over list
colors = ["Red", "Green", "Blue", "Orange"]
for color in colors:
    print(color)
    for i in color:
        print(i)

# for loop over range
for i in range(7):
    print(i)

# between two numbers
for i in range(1,50):
    print(i)

# three value
for i in range(1,50,4):
    print(i)